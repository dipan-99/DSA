package Loops;

public class Printing_Numbers {
    public static void main(String[] args) {

        // decreasing
        int num1 = 10;

        while (num1 > 0) {
            System.out.println(num1);
            num1--;
        }

        // increasing
        int num2 = 1;

        while (num2 <= 10) {
            System.out.println(num2);
            num2--;
        }


    }
}